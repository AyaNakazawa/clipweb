import cgi

# ----------------------------------------------------------------
# Class
# ----------------------------------------------------------------

class CGI:

    def __init__(cls):
        cls.FORM = cgi.FieldStorage()
        print("Content-type: text/html; charset=UTF-8")
        print()

    # ----------------------------------------------------------------
    # Function
    # ----------------------------------------------------------------

    def get(cls, _label):
        if _label not in cls.FORM:
            return None
        return cls.FORM.getvalue(_label, "")

