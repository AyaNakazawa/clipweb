import cgi

# ----------------------------------------------------------------
# Class
# ----------------------------------------------------------------

class CGI:

    # ----------------------------------------------------------------
    # Function
    # ----------------------------------------------------------------

    # ----------------------------------------------------------------
    # Get CGI form value
    @staticmethod
    def get(_label):
        # print("[FUNC]")
        # print("CGI.get({0})".format(_label))
        FORM = cgi.FieldStorage()
        if _label not in FORM:
            return None
        return FORM.getvalue(_label, "")

    # ----------------------------------------------------------------
    # CGI Init
    @staticmethod
    def init():
        print("Content-type: text/html; charset=UTF-8")
        print()
        # print("[INIT]")
        # print("CGI")

# ----------------------------------------------------------------
# Ready
# ----------------------------------------------------------------

CGI.init()
